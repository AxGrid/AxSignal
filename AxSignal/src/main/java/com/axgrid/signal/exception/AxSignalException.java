package com.axgrid.signal.exception;

public class AxSignalException extends Exception {
    public AxSignalException() {super();}
    public AxSignalException(String message) {super(message);}
    public AxSignalException(Throwable t) {super(t);}
    public AxSignalException(String message, Throwable t) {super(message, t);}
}
